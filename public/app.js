var fcjs_contextMenu = document.getElementsByClassName('fcjs_contextMenu')[0];
var fcjs_fontName = document.getElementsByClassName('fcjs_fontName')[0];

var fcjs_prev = document.getElementsByClassName('fcjs_prev')[0];
